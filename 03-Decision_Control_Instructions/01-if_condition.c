/*
    If Statement : Contains an expression that can be "true" or "false".

    General syntax :
                                if(condition)
                                {
                               .....code....
                            }
*/
/*
    A decision can be made on any expression
                    zero = false
                    one  = true
Example :
*/
#include <stdio.h>
int main(){
    int x;
    x = 5;
    if(x==5)
        printf("yes\n");

//Example :
    if(x!=5)
        printf("no\n");

return 0;
}
































