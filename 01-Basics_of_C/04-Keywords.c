/*  Keywords in C   */

/*
    Keywords` :- Keywords are those word which has special meaning in C language.

    There are 32 keywords in the C language.
    - auto :- auto define a local variable as having a local lifetime.

    - break :- break pass control out of the collection of statment.

    - case :- case make the statment in different cateogories.

    - char :- char is type of variable, which store the alphabets.

    - int :- int is a type variable, which store the (0, 1, 3, 4, 5, 6, 7, 8, 9)integer.

    - const :- const keyword used for the define constent variable.
    
    - default :- default keyword are used for the execution of that point which doesn't exist as per  user condition.

    - do :- do keyword is usually use for the execute a set of statement run mininum a single time.

    - double :- double keyword repersent a double precision of floating data type.
    
    - else :- else keyword are use when if statment become false.
    
    - extern :- extern keyword indicate that the actually storage & initial value of variable was define else where.
    
    - for :-  for keyword are use for the execution of loop statement.
    
    - while :-  while keyword also use for the execution of loop statement.
    
    - goto :- goto  keyword are used for the unconditional transfer control.
    
    - if :-  if keyword's are use for the check the condition & statment will execute if condition true.
    
    - long :- long keyword can be applied to the base datatype i.e int, float, double etc.
    
    - register :- register keywords tells the compiler to store the variable in CPU register.
    
    - return :- return keyword is used to return a value from a function.
    
    - short :- short keyword is use for the reduce 16 bits size of integer data type.
    
    - signed :- signed keyword is used to define a signed variable which can store both positive & negative value.
    
    - sizeof :- keyword is use for the returns the amount of memory in bytes that a variable or data type occupies.
    
    - static :- keyword is used to define a variable keeps it's value after the function end.
    
    - struct :-  Structure is a user defined data type which define by the "struct" keyword. It is used to group different data types together.
    
    - switch :- switch keyword is use for the selects of one of many code blocks to be executed.

    - typedef :- typedef keyword is used to create an alias for a data type.

    - union :- Union is a user defined data type which define by the "union" keyword. It is used to group different data types together but only one member can be used at a time.

    - unsigned :- unsigned keyword is used to define an unsigned variable which can store only positive value.
    
    - void :- void keyword is used to define a function that does not return any value. it may be use at the place of int, char, float, double etc.
    
    - volatile :- keyword is used to tell the compiler that the value of a variable may change at any time, without any action being taken by the code the compiler finds nearby.
    
    - enum :- enum keyword is used to define a user defined data type which can store a set of named integer constants.*/
    